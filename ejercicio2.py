PrimerNumero = int(input("Digite el primer numero entero: "))
SegundoNumero = int(input("Digite el segundo numero entero: "))
ResultadoCociente = PrimerNumero//SegundoNumero
ResultadoResto = PrimerNumero%SegundoNumero

print ("El resultado de dividir " + str(PrimerNumero) +" entre " + str(SegundoNumero) + " da un cociente de " +  str(ResultadoCociente) + " y un resto de "+ str(ResultadoResto))